package Objects;

public class System {

    private int clan;
    private int maxPop;
    private int pop;
    private int popSec;

    public System(int clan, int maxPop, int pop, int popSec) {
        this.clan = clan;
        this.maxPop = maxPop;
        this.pop = pop;
        this.popSec = popSec;
    }
}
