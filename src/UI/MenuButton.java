package UI;

public class MenuButton {

    int posx;
    int posy;
    String name;


    public MenuButton(int x, int y) {
        this.posx = x;
        this.posy = y;
    }

    public int getPosx() {
        return this.posx;
    }

    public int getPosy() {
        return this.posy;
    }

}
